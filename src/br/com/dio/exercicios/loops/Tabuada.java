package br.com.dio.exercicios.loops;



public class Tabuada {

	public static void main(String[] args) {
		
	   
	   
	   System.out.println("Tabuada d 1 a 10.");
	   
		for(int i = 1; i <= 10; i++) {
			for(int j = 0; j <= 10; j++) {
				System.out.println(i + " x " + j + " = " + i * j);
			}
		}
		
	}
}
